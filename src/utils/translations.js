export function useTranslation() {
  const branding = useBranding();
  const lang = branding?.language || 'en';
  
  const t = (key) => {
    const langTrans = translations[lang] || translations['en'];
    return langTrans[key] || translations['en'][key] || key;
  };
  
  return { t, lang };
}
